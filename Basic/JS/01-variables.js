//tipos de variables
//var
var helloWorld = "Hola JavaScript"
console.log(helloWorld)

helloWorld = "Hola de nuevo"
console.log(helloWorld)

//let
let helloWorld2 = "Hola Mundo dos"
console.log(helloWorld2)

helloWorld2 = "Hola de nuevo dos"
console.log(helloWorld2)


//const
const helloWorld3 = "Hola mundo tres"
console.log(helloWorld3)

helloWorld3 = "Hola de nuevo mundo tres"
console.log(helloWorld3)